package models;

public class User {

    private String saludo;
    private String numeroPre;
    private String valor;
    private String numeroPost;


    public User() {
    }

    public String getNumeroPre() {
        return numeroPre;
    }

    public String getSaludo() {
        return saludo;
    }

    public String getValor() {
        return valor;
    }

    public String getNumeroPost() {
        return numeroPost;
    }

}